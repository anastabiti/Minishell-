#include "../minishell.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*s;
	char	*d;

	s = (char *)src;
	d = (char *)dst;
	if (s == NULL && d == NULL)
		return (NULL);
	if (d < s && (*d != '\0' || *s != '\0'))
		return (ft_memcpy(dst, src, len));
	if (s < d)
	{
		while (len > 0)
		{
			d[len - 1] = s[len - 1];
			len--;
		}
	}
	return (dst);
}
